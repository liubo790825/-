    public class Form_ModuleInstanceService : RepositoryFactory<Form_ModuleInstanceEntity>, IForm_ModuleInstanceService
    {
        #region 获取数据
        /// <summary>
        /// 获取列表
        /// </summary>
        /// <param name="pagination">分页</param>
        /// <param name="queryJson">查询参数</param>
        /// <returns>返回分页列表</returns>
        public IEnumerable<Form_ModuleInstanceEntity> GetPageList( Pagination pagination, string queryJson)
        {
             var expression = LinqExtensions.True<Form_ModuleInstanceEntity>();
             //参考代码
             var queryParam = queryJson.ToJObject();
             if (!queryParam["FrmContentId"].IsEmpty()){
                 string FrmContentId = queryParam["FrmContentId"].ToString();
                 expression = expression.And(t => t.FrmContentId.Equals(FrmContentId));
             }
             //如果有字段2，字段3也这样写...
             return this.BaseRepository().FindList(expression,pagination);
        }
        /// <summary>
        /// 获取列表
        /// </summary>
        /// <param name="queryJson">查询参数</param>
        /// <returns>返回列表</returns>
        public IEnumerable<Form_ModuleInstanceEntity> GetList( string queryJson)
        {
            return this.BaseRepository().IQueryable().ToList();
        }
        /// <summary>
        /// 获取实体
        /// </summary>
        /// <param name="keyValue">主键值</param>
        /// <returns></returns>
        public Form_ModuleInstanceEntity GetEntity( string keyValue)
        {
            return this.BaseRepository().FindEntity(keyValue);
        }
        #endregion

        #region 提交数据
        /// <summary>
        /// 删除数据
        /// </summary>
        /// <param name="keyValue">主键</param>
        public void RemoveForm( string keyValue)
        {
            this.BaseRepository().Delete(keyValue);
        }
        /// <summary>
        /// 保存表单（新增、修改）
        /// </summary>
        /// <param name="keyValue">主键值</param>
        /// <param name="entity">实体对象</param>
        /// <returns></returns>
        public void SaveForm( string keyValue, Form_ModuleInstanceEntity entity)
        {
            if (!string.IsNullOrEmpty(keyValue))
            {
                entity.Modify(keyValue);
                this.BaseRepository().Update(entity);
            }
            else
            {
                entity.Create();
                this.BaseRepository().Insert(entity);
            }
        }
        #endregion
    }
}
